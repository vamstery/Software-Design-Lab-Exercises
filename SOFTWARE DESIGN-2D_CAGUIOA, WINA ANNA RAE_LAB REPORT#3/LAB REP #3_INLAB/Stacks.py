my_stack = []

# append() function to push
# element in the my_stack
my_stack.append('a')
my_stack.append('i')
my_stack.append('n')
my_stack.append('w')

print(my_stack)

# pop() function to pop
# element from my_stack in
# LIFO order
print('\nElements poped from my_stack:')
print(my_stack.pop())
print(my_stack.pop())
print(my_stack.pop())
print(my_stack.pop())

print('\nmy_stack after elements are poped:')
print(my_stack)